#include <unistd.h>

void	print_grid(int **grid)
{
	int		y;
	int		x;
	char	n;

	y = 2;
	x = 2;
	while (y < 6)
	{
		while (x < 6)
		{
			n = grid[y][x] + '0';
			write(1, &n, 1);
			x++;
		}
		write(1, "\n", 1);
		x = 2;
		y++;
	}
}
